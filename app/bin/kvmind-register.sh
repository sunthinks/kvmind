#!/bin/bash
# KVMind Device Registration (optional)
# Registers this device with a configured cloud backend (leave backend_url empty to disable).
#
# Prerequisites (created by install.sh):
#   /etc/kdkvm/device.uid    — unique device identifier
#   /etc/kdkvm/config.yaml   — backend_url (optional, empty = skip registration)
#
# Runs:
#   - Once at end of install.sh (first registration attempt)
#   - On boot via kvmind-register.timer (retry if first attempt failed)
#
# If you are running in fully local/air-gapped mode, leave backend_url empty —
# this script will exit cleanly without contacting any cloud service.

set -euo pipefail

UID_FILE="/etc/kdkvm/device.uid"
CONFIG_FILE="/etc/kdkvm/config.yaml"
TOKEN_FILE="/etc/kdkvm/device.token"
REG_FLAG="/etc/kdkvm/.registered"
LOG_TAG="kvmind-register"

log() { logger -t "$LOG_TAG" "$*"; echo "[kvmind-register] $*"; }

# ── Already registered? ──
if [[ -f "$REG_FLAG" && -f "$TOKEN_FILE" && "${1:-}" != "--force" ]]; then
    log "Already registered. Use --force to re-register."
    exit 0
fi

# ── UID is required (generated by install.sh) ──
if [[ ! -f "$UID_FILE" ]]; then
    log "ERROR: $UID_FILE not found. Run install.sh first."
    exit 1
fi
DEVICE_UID=$(tr -d '[:space:]' < "$UID_FILE")
if [[ -z "$DEVICE_UID" ]]; then
    log "ERROR: $UID_FILE is empty."
    exit 1
fi

# ── Backend URL from config (empty = no cloud, skip registration) ──
BACKEND_URL=""
if [[ -f "$CONFIG_FILE" ]]; then
    VAL=$(grep -m1 "^[[:space:]]*backend_url:" "$CONFIG_FILE" 2>/dev/null | sed 's/^[^:]*:[[:space:]]*//' | tr -d '"' | tr -d "'" || true)
    [[ -n "$VAL" ]] && BACKEND_URL="$VAL"
fi
if [[ -z "$BACKEND_URL" ]]; then
    log "No backend_url configured — running in local/air-gapped mode, skipping registration."
    exit 0
fi

# ── MAC address ──
MAC=""
for iface in eth0 end0 enp0s3 wlan0; do
    MAC=$(ip link show "$iface" 2>/dev/null | grep ether | awk '{print $2}' || true)
    [[ -n "$MAC" ]] && break
done
[[ -z "$MAC" ]] && log "WARN: Could not determine MAC address"

# ── Registration secret (proves device ownership at bind time) ──
REG_SECRET=""
SECRET_FILE="/etc/kdkvm/registration.secret"
if [[ -f "$SECRET_FILE" ]]; then
    REG_SECRET=$(tr -d '[:space:]' < "$SECRET_FILE")
fi

# ── Register ──
REGISTER_URL="${BACKEND_URL}/api/devices/register"
PAYLOAD="{\"uid\":\"$DEVICE_UID\",\"macAddress\":\"${MAC:-unknown}\",\"registrationSecret\":\"$REG_SECRET\"}"

log "Registering: UID=$DEVICE_UID MAC=${MAC:-unknown} → $REGISTER_URL"

RESULT=$(curl -s -X POST "$REGISTER_URL" \
    -H "Content-Type: application/json" \
    -d "$PAYLOAD" \
    --connect-timeout 10 \
    --max-time 15 2>/dev/null) || true

if [[ -z "$RESULT" ]]; then
    log "WARN: Cloud unreachable. Will retry on next boot."
    exit 0
fi

SAFE_LOG=$(echo "$RESULT" | sed 's/"deviceToken":"[^"]*"/"deviceToken":"***"/g')
log "Response: $SAFE_LOG"

# Check for success and extract device token
if echo "$RESULT" | grep -q '"code":200'; then
    # Extract deviceToken from response JSON
    DEVICE_TOKEN=$(echo "$RESULT" | grep -o '"deviceToken":"[^"]*"' | sed 's/"deviceToken":"//;s/"//' || true)

    mount -o remount,rw / 2>/dev/null || true
    echo "$DEVICE_UID $(date -Iseconds)" > "$REG_FLAG"

    if [[ -n "$DEVICE_TOKEN" ]]; then
        echo "$DEVICE_TOKEN" > "$TOKEN_FILE"
        chmod 600 "$TOKEN_FILE"
        log "Device token saved to $TOKEN_FILE"
    fi

    mount -o remount,ro / 2>/dev/null || true
    log "Registration complete: $DEVICE_UID"
elif echo "$RESULT" | grep -q '"code":409'; then
    # 409 = device already registered — normal on reinstall, treat as success
    log "Device already registered (409). No action needed."
else
    SAFE_LOG=$(echo "$RESULT" | sed 's/"deviceToken":"[^"]*"/"deviceToken":"***"/g')
    log "WARN: Registration may have failed. Response: $SAFE_LOG"
    exit 0
fi
